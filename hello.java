public class hello {
    public static void main(String[] args) {
        int a = 20;
        int b = 22;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        int h = 1;
        System.out.println(h);
        System.out.println(++h);
        System.out.println(h);
        System.out.println(h++);
        System.out.println(h);
    }
}